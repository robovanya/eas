    private void sp_rokStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_sp_rokStateChanged
        Integer actYear;
        actYear = (Integer) sp_rok.getValue();
        if ((lastYear != null) && !sp_rok.getValue().equals(lastYear)) {
            actYear = (Integer) sp_rok.getValue();
            System.out.println("STS: "+ actYear);    
            eoc.dbdata.DBtableColumn keyCol = 
                brwGrafikon.currentGraf.currentDim.getPrimaryKeyColumn();
            
            eoc.dbdata.DBtableColumn[] dbc = null;
            try {
                dbc = brwGrafikon.currentGraf.getTimeDimensionColumns(actYear, lastMonth, keyCol);
            } catch (ParseException ex) {
                Logger.getLogger(Pnl_GrafikonHeader.class.getName()).log(Level.SEVERE, null, ex);
            }
            if (dbc != null) {
            for (int e = 0; e < dbc.length; e++) {
                dbc[e].setCellRenderer(new DimensionCellRenderer());;
            }
            }
            brwGrafikon.currentGraf.replaceDataColumns(dbc);
            Object oData[][] = null;
        }
        lastYear = actYear;
    }//GEN-LAST:event_sp_rokStateChanged

    private void sp_mesiacStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_sp_mesiacStateChanged
        Integer actMonth;
        actMonth = (Integer) sp_mesiac.getValue();
        if ((lastMonth != null) && !sp_mesiac.getValue().equals(lastMonth)) {
            actMonth = (Integer) sp_mesiac.getValue();
            System.out.println("STS: "+ actMonth);     
            eoc.dbdata.DBtableColumn keyCol = 
                brwGrafikon.currentGraf.currentDim.getPrimaryKeyColumn();
            
            eoc.dbdata.DBtableColumn[] dbc = null;
            try {
                dbc = brwGrafikon.currentGraf.getTimeDimensionColumns(lastYear, actMonth, keyCol);
            } catch (ParseException ex) {
                Logger.getLogger(Pnl_GrafikonHeader.class.getName()).log(Level.SEVERE, null, ex);
            }
            if (dbc != null) {
            for (int e = 0; e < dbc.length; e++) {
                dbc[e].setCellRenderer(new DimensionCellRenderer());
            }
            }
            brwGrafikon.currentGraf.replaceDataColumns(dbc);
            Object oData[][] = null;
        }
        lastMonth = actMonth;
    }//GEN-LAST:event_sp_mesiacStateChanged

